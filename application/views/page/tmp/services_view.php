    <h2 id="ustanovka_videjnabludenie">Установка видеонаблюдения</h2>
        <p>
            Мы выполняем работы по установки систем видеонаблюдения на объекты любой степени сложности.<br />
            Опыт наших специалистов выполняющих монтаж элементов системы видеонаблюдения исчисляется годами, 
            что позволяет нам утверждать, что мы выполняем свою работу на отлично!</p>
        <p>
            Каждая <strong>система видеонаблюдения</strong> состоит из нескольких основных компонентов. 
            Это в первую очередь камеры видеонаблюдения которые снимают картинку и передают ее на следующий 
            элемент системы видеорегистратор, который записывает полученное с камер видеонаблюдения 
            изображение на жесткий диск. Следующим элементом системы является монитор, на который 
            выводятся изображения с камер наблюдения обработанных перед этим видеорегистратором. 
            Зачастую в <strong>систему видеонаблюдения</strong> устанавливаемую в домах входит также и
            <a href="#ustanovka_domofonov">установка видеодомофона</a> с электронным замком, 
            что позволяет видеть пришедшего и не 
            утруждая себя впустить его в дом воспользовавшись электронным замком просто нажав на кнопку.
        </p>
        <p style="font-weight: bold;">Обслуживание систем видеонаблюдения</p>
        <p>
            <strong>Обслуживанием систем видеонаблюдения</strong>
            является периодический контроль за состоянием системы и восстановление или 
            же ремонт модулей входящих в систему. 
            Камеры наблюдения особенно это касается уличных, нуждаются в обслуживании термокожухов, 
            объективов - устранения загрязнённости стекол и оптики. 
            В агрессивных условиях эксплуатации крайнюю важность имеет постоянный контроль вышеуказанных 
            элементов видеокамер, потому как даже не сильная загрязненность камер сильно 
            ухудшает качество изображения, получаемого с них. 
            В случае видеорегистраторов, необходим уход за внутренними частями и элементами 
            системы охлаждения от пыли ~ 1 раз в 3-6 месяцев. 
            Кабельные магистрали зачастую не требуют обслуживания, однако если произойдет обрыв 
            магистрали по причине форс-мажорных обстоятельств или спланированной диверсии 
            может понадобится частичная или же полная замена кабельных магистралей.
        </p>
        <p style="font-weight: bold;">Зачем нужно видеонаблюдение?</p>
        <p>
            Монтаж камер наблюдения в торговых и выставочных помещениях позволит предотвратить попытки кражи. 
            Уличные или внешние камеры видеонаблюдения дадут Вам возможность контролировать 
            происходящее за границами объекта. Ночное системы слежения 
            обеспечивает надежный контроль над охраняемым объектом в ночное время суток. 
            Поворотные и скоростные камеры позволяют оперативно проследить за 
            перемещениями подозрительных лиц находящихся на или вблизи объекта за которым ведется наблюдение. 
            А компактные потайные камеры видеонаблюдения скроют Вашу заинтересованность, и наблюдение 
            расскажет о происходящем в Ваше отсутствие.
        </p>
        <p>
            Использование камер для слежения обходится намного дешевле чем использования охранников. 
            При многократном увеличении качества работы <em>системы видеонаблюдения</em> и контроля не подвержены усталости, 
            болезни и многим другим людским слабостям.
        </p>
    
    
