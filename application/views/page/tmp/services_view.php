    <h2 id="ustanovka_kondicionerov">Установка кондиционеров</h2>
        <p>
            Мы выполняем услуги по продаже и установке кондиционеров в городе Харькове и пригороде.<br />
            Специалисты нашей фирмы с окажут Вам квалифицированную консультацию и выполнят качественную 
            установку кондиционера.
        </p>
        <p>
            Установка кондиционера является сложным процессом, который требует специальных знаний и 
            практического опыта, а главное, что при монтаже кондиционера необходимо использовать 
            специальное дорогостоящее оборудование. Мы предлагаем Вам качественную установку любых типов 
            кондиционеров, т.к. у нас имеется профессиональный монтажный отдел, располагающий 
            необходимым оборудованием и отличными специалистами, знатоками своего дела, 
            которые имеют многолетний опыт работы. При проведении работ по установке применяются 
            исключительно высококачественные материалы. Мы выполняем <strong>установку кондиционеров</strong>
            не только в Харькове но и в ближайших городах Харьковской области.
        </p>
        <p><strong>Монтаж оконного кондиционера</strong></p>
        <p>
            Монтаж этого типа кондиционеров отличаются тем, что наибольшая трудность в установке здесь 
            заключается не столько в работе с самим кондиционером, сколько в аккуратном размещении его 
            в окне или в форточке. Все дело в том, что оконный кондиционер состоит только из одного блока, 
            корпус которого компактный и прочный. 
            Подключение такого типа кондиционера обычно происходит через обычную розетку, так что работа 
            монтажник в случае установки кондиционера такого типа более 
            схожа работе строителя-отделочника или плотника. В общих случаях монтаж этого класса длится не 
            более двух часов.
        </p>
        <p><strong>Установка сплит-систем кассетного типа кондиционеров</strong></p>
        <p>
            Отличительная черта при установке систем данного типа состоит в том, 
            что здесь один из блоков необходимо монтировать за подвесным потолком, 
            закрепив его предварительно к капитальному. То есть кроме специалистов по установке в состав 
            бригады должны входить еще и те люди, которые обеспечивают собственно вырезание соответствующего 
            отверстия под установку блока кондиционера. Эта задача значительно упрощается если в помещении 
            установлен подвесной потолок типа &laquo;Армстронг&raquo; &ndash; размер панелей такого потолка 
            полностью соответствует размерам блока. Зачастую в конструкции данного типа кондиционера 
            имеется помпа, которая перекачивает воду в дренаж, откуда она уже отводится на улицу или же в санузел.
        </p>
        <p><strong>Установка настенных бытовых кондиционеров (сплит-систем настенного типа)</strong></p>
        <p>
            Установка кондиционеров данного типа намного сложнее, чем обычная установка сплит-систем оконного типа. 
            Тут все дело в том, что здесь необходимо применение специализированного оборудования. 
            Например, такого оборудования как перфоратор, вальцовка для труб, вакуумный насос и многое 
            другое оборудование. Поэтому для уменьшения времени установки ее проводят одновременно две бригады монтажников. 
            Такой подход дает возможность выполнить установку и наладку кондиционера в два - три часа. 
            <strong>Установка кондиционеров в Харькове</strong> может проводиться типах помещений &ndash; 
            с оконченным или неоконченным ремонтом.
        </p>
    
